// Testes, TDD e BDD

/* 
— Testes = testes automatizados, unitarios, e ferramentas de testes.

— TDD =  Desenvolvimento Orientado por Testes. Ele se baseia na aplicação de pequenos ciclos de repetições.
Em cada um deles, um teste é aplicado.

— BDD = BDD é técnica de desenvolvimento ágil que visa integrar regras de negócios com linguagem de programação,
focando o comportamento do software. Além disso, pode-se dizer também, que BDD é a evolução do TDD. Isto porque,
os testes ainda orientam o desenvolvimento, ou seja, primeiro se escreve o teste e depois o código
*/